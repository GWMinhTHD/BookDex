                  onClick={logout}
                  className="w-full text-center text-white bg-red-600 hover:bg-red-700 px-3 py-2 rounded-md text-base font-medium focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-offset-gray-800 focus:ring-red-500"
